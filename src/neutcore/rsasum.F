      ASUM( 1 , 4 , 11 ) = AP1C(24)*ETABW(24)*RESSGN(24)
      ASUM( 1 , 4 , 12 ) = AP1C(17)*ETABW(17)*RESSGN(17)
      ASUM( 1 , 4 , 13 ) = AP1C(25)*ETABW(25)*RESSGN(25)
