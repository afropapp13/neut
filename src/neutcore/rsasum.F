      ASUM( 1 , 5 , 7 ) = A0PC(13)*ETABW(13)*RESSGN(13)
      ASUM( 1 , 5 , 9 ) = 0.
      ASUM( 1 , 5 , 8 ) = A0PC( 2)*ETABW( 2)*RESSGN( 2)
     $                  + A0PC( 9)*ETABW( 9)*RESSGN( 9)
      ASUM( 1 , 5 , 10 ) = A0PC(11)*ETABW(11)*RESSGN(11)
      ASUM( 1 , 5 , 11 ) = A0PC(24)*ETABW(24)*RESSGN(24)
      ASUM( 1 , 5 , 12 ) = A0PC(17)*ETABW(17)*RESSGN(17)
      ASUM( 1 , 5 , 13 ) = A0PC(25)*ETABW(25)*RESSGN(25)
      ASUM( 1 , 5 , 14 ) = A0PC(27)*ETABW(27)*RESSGN(27)

      ASUM( 1 , 6 , 1 ) = A0MC( 6)*ETABW( 6)*RESSGN( 6)
      ASUM( 1 , 6 , 2 ) = A0MC( 4)*ETABW( 4)*RESSGN( 4)
     $                  + A0MC( 7)*ETABW( 7)*RESSGN( 7)
      ASUM( 1 , 6 , 3 ) = A0MC(23)*ETABW(23)*RESSGN(23)
      ASUM( 1 , 6 , 4 ) = A0MC(14)*ETABW(14)*RESSGN(14)
     $                  + A0MC(19)*ETABW(19)*RESSGN(19)
      ASUM( 1 , 6 , 5 ) = A0MC( 1)*ETABW( 1)*RESSGN( 1)
     $                  + A0MC(16)*ETABW(16)*RESSGN(16)
     $                  + A0MC(26)*ETABW(26)*RESSGN(26)                  
      ASUM( 1 , 6 , 6 ) = A0MC(21)*ETABW(21)*RESSGN(21)
      ASUM( 1 , 6 , 7 ) = A0MC(13)*ETABW(13)*RESSGN(13)
      ASUM( 1 , 6 , 9 ) = 0.
      ASUM( 1 , 6 , 8 ) = A0MC( 2)*ETABW( 2)*RESSGN( 2)
     $                  + A0MC( 9)*ETABW( 9)*RESSGN( 9)
      ASUM( 1 , 6 , 10 ) = A0MC(11)*ETABW(11)*RESSGN(11)
      ASUM( 1 , 6 , 11 ) = A0MC(24)*ETABW(24)*RESSGN(24)
      ASUM( 1 , 6 , 12 ) = A0MC(17)*ETABW(17)*RESSGN(17)
      ASUM( 1 , 6 , 13 ) = A0MC(25)*ETABW(25)*RESSGN(25)
      ASUM( 1 , 6 , 14 ) = A0MC(27)*ETABW(27)*RESSGN(27)

      ASUM( 2 , 1 , 1 ) = AM3C( 6)*ETABW( 6)*RESSGN( 6)
      ASUM( 2 , 1 , 2 ) = AM3C( 5)*ETABW( 5)*RESSGN( 5)
     $                  + AM3C( 8)*ETABW( 8)*RESSGN( 8)
      ASUM( 2 , 1 , 3 ) = AM3C(23)*ETABW(23)*RESSGN(23)
      ASUM( 2 , 1 , 4 ) = AM3C(15)*ETABW(15)*RESSGN(15)
     $                  + AM3C(20)*ETABW(20)*RESSGN(20)
      ASUM( 2 , 1 , 5 ) = AM3C( 1)*ETABW( 1)*RESSGN( 1)
     $                  + AM3C(16)*ETABW(16)*RESSGN(16)
     $                  + AM3C(26)*ETABW(26)*RESSGN(26)                  
      ASUM( 2 , 1 , 6 ) = AM3C(22)*ETABW(22)*RESSGN(22)
      ASUM( 2 , 1 , 7 ) = AM3C(13)*ETABW(13)*RESSGN(13)
      ASUM( 2 , 1 , 9 ) = 0.
      ASUM( 2 , 1 , 8 ) = AM3C( 3)*ETABW( 3)*RESSGN( 3)
     $                  + AM3C(10)*ETABW(10)*RESSGN(10)
      ASUM( 2 , 1 , 10 ) = AM3C(12)*ETABW(12)*RESSGN(12)
      ASUM( 2 , 1 , 11 ) = AM3C(24)*ETABW(24)*RESSGN(24)
      ASUM( 2 , 1 , 12 ) = AM3C(17)*ETABW(17)*RESSGN(17)
      ASUM( 2 , 1 , 13 ) = AM3C(25)*ETABW(25)*RESSGN(25)
      ASUM( 2 , 1 , 14 ) = AM3C(28)*ETABW(28)*RESSGN(28)
      
      ASUM( 2 , 2 , 1 ) = AM1C( 6)*ETABW( 6)*RESSGN( 6)
      ASUM( 2 , 2 , 2 ) = AM1C( 5)*ETABW( 5)*RESSGN( 5)
     $                  + AM1C( 8)*ETABW( 8)*RESSGN( 8)
      ASUM( 2 , 2 , 3 ) = AM1C(23)*ETABW(23)*RESSGN(23)
      ASUM( 2 , 2 , 4 ) = AM1C(15)*ETABW(15)*RESSGN(15)
     $                  + AM1C(20)*ETABW(20)*RESSGN(20)
      ASUM( 2 , 2 , 5 ) = AM1C( 1)*ETABW( 1)*RESSGN( 1)
     $                  + AM1C(16)*ETABW(16)*RESSGN(16)
     $                  + AM1C(26)*ETABW(26)*RESSGN(26)                  
      ASUM( 2 , 2 , 6 ) = AM1C(22)*ETABW(22)*RESSGN(22)
      ASUM( 2 , 2 , 7 ) = AM1C(13)*ETABW(13)*RESSGN(13)
      ASUM( 2 , 2 , 9 ) = 0.
      ASUM( 2 , 2 , 8 ) = AM1C( 3)*ETABW( 3)*RESSGN( 3)
     $                  + AM1C(10)*ETABW(10)*RESSGN(10)
      ASUM( 2 , 2 , 10 ) = AM1C(12)*ETABW(12)*RESSGN(12)
      ASUM( 2 , 2 , 11 ) = AM1C(24)*ETABW(24)*RESSGN(24)
      ASUM( 2 , 2 , 12 ) = AM1C(18)*ETABW(18)*RESSGN(18)
      ASUM( 2 , 2 , 13 ) = AM1C(25)*ETABW(25)*RESSGN(25)
      ASUM( 2 , 2 , 14 ) = AM1C(28)*ETABW(28)*RESSGN(28)
      
      ASUM( 2 , 3 , 1 ) = AP3C( 6)*ETABW( 6)*RESSGN( 6)
      ASUM( 2 , 3 , 2 ) = AP3C( 5)*ETABW( 5)*RESSGN( 5)
     $                  + AP3C( 8)*ETABW( 8)*RESSGN( 8)
      ASUM( 2 , 3 , 3 ) = AP3C(23)*ETABW(23)*RESSGN(23)
      ASUM( 2 , 3 , 4 ) = AP3C(15)*ETABW(15)*RESSGN(15)
     $                  + AP3C(20)*ETABW(20)*RESSGN(20)
      ASUM( 2 , 3 , 5 ) = AP3C( 1)*ETABW( 1)*RESSGN( 1)
     $                  + AP3C(16)*ETABW(16)*RESSGN(16)
     $                  + AP3C(26)*ETABW(26)*RESSGN(26)                  
      ASUM( 2 , 3 , 6 ) = AP3C(22)*ETABW(22)*RESSGN(22)
      ASUM( 2 , 3 , 7 ) = AP3C(13)*ETABW(13)*RESSGN(13)
      ASUM( 2 , 3 , 9 ) = 0.
      ASUM( 2 , 3 , 8 ) = AP3C( 3)*ETABW( 3)*RESSGN( 3)
