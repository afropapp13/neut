     $                  + A0PC( 7)*ETABW( 7)*RESSGN( 7)
      ASUM( 1 , 5 , 3 ) = A0PC(23)*ETABW(23)*RESSGN(23)
      ASUM( 1 , 5 , 4 ) = A0PC(14)*ETABW(14)*RESSGN(14)
     $                  + A0PC(19)*ETABW(19)*RESSGN(19)
      ASUM( 1 , 5 , 5 ) = A0PC( 1)*ETABW( 1)*RESSGN( 1)
