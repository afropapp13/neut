      FUNCTION RSMASS(AMP,AM1,AM2,WID1)
C
C      T.KAJITA 19-OCT-85  FROM RMASS
C
C     DETERMINE MASS ,CONSIDERING BREIT-WIGNER FORMULA
C    THE HIGHEST BOUND OF THE MASS IS CONSIDERED BUT THE LOWEST MASS IS
C                                                                  NOT.
C     AMP  ; MASS OF PARENT PARTICLE     ( NON RESONANCE)
C     AM1  ;    "    DECAYING PARTICLE 1 (*****RESONANCE*****)
C     AM2  ;    "    THE PARTNER OF PARTICLE 1
C     WID1 ; FULL WIDTH OF THE MASS OF THE PARTICLE 1
C
C    1 RNT=(RNDM(DUM)-0.5)*3.14159
    1 RNT=(RLU(IDUM)-0.5)*3.14159
      RSMASS=TAN(RNT)*WID1/2. + AM1
C
C -- COSIDER KINEMATICS
      IF((RSMASS+AM2).GE.AMP)GO TO 1
      IF(RSMASS.LE.280.)GO TO 1
C
C
      RETURN
      END
