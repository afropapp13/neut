*     -----------------------------------------------------
      FUNCTION efgampi0pt(GK)
*     -----------------------------------------------------
*     
*     ( purpose )
*       RETURN TOTAL CROSS SECTION OF
C          GAMMA,p ---> pi0p
*
*     ( input ) 
*          GK   : gamma energy(MEV)
*
*     ( output ) 
*       efgampi0pt : TOTAL CROSS SECTION (MICRO BARN/STR.)
*
C
C -- REFERENCE
C      SAID PWA as of Jan-Feb, 2010
C      Solution: SP09K 2700 MEV P(199)
C      (http://gwdac.phys.gwu.edu/analysis/pr_analysis.html)
C                     - P. de Perio
C
      DIMENSION NRG(91),CRS(91)
      DATA NRG/144.,
     &150.,160.,170.,180.,190.,200.,210.,220.,230.,
     &240.,250.,260.,270.,280.,290.,300.,310.,320.,
     &330.,340.,350.,360.,370.,380.,390.,400.,410.,
     &420.,430.,440.,450.,460.,470.,480.,490.,500.,
     &510.,520.,530.,540.,550.,560.,570.,580.,590.,
     &600.,610.,620.,630.,640.,650.,660.,670.,680.,
     &690.,700.,710.,720.,730.,740.,750.,760.,770.,
     &780.,790.,800.,810.,840.,870.,900.,930.,960.,
     &990.,1020.,1050.,1080.,1110.,1140.,1170.,1200.,1230.,
     &1260.,1290.,1320.,1350.,1380.,1410.,1440.,1470.,2000./

      DATA CRS/0,
     &0.481,2.34,5.14,8.94,14,20.6,29.4,41,56,
     &75.6,100,131,168,208,248,282,303,309,
     &301,282,258,232,207,184,163,145,128,
     &114,102,91.6,82.4,74.4,67.4,61.3,56,51.4,
     &47.3,43.7,40.6,37.8,35.4,33.3,31.4,29.9,28.5,
     &27.5,26.6,26,25.7,25.6,25.8,26.3,27,28.1,
     &29.6,32.1,35.3,37.3,39,40.2,40.8,40.9,40.4,
     &39.4,38.2,36.7,35,29.7,24.9,21.7,21.1,23.1,
     &26.1,27.1,24.9,20.9,17.2,14.5,12.7,11.7,11.2,
     &11.1,11.2,11.5,11.7,11.9,11.9,11.8,11.4,3.55/

       efgampi0pt=0.
      IF(GK.LT.NRG(1))RETURN
      IF(GK.GT.NRG(91)) then
         efgampi0pt=-1.
         RETURN
      ENDIF

      DO 100 I=1,91
         IF (GK.LT.NRG(I)) GOTO 110
 100  CONTINUE

 110  CONTINUE
      efgampi0pt= CRS(I-1)
     $       + (CRS(I)-CRS(I-1))/(NRG(I)-NRG(I-1))
     $        *(GK-NRG(I-1))

      RETURN
      END
