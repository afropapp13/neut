*     -----------------------------------------------------
      FUNCTION EFGAMMN(GK,COST)      
*     -----------------------------------------------------
*     
*     ( purpose )
*        RETURN DIFFERENCIAL CROSS SECTION OF
*            GAMMA,N ---> PI-,P
*
*     ( input ) 
*        GK     : GAMMA ENERGY(MEV)
*        COST   : COS(THETA)
*      
*     ( output ) 
*        GAMMN  : differential cross-section(mb/sr)
C
C -- REFERENCES
C      INS-REPORT-256   MARCH,1976
C      PRIVATE COMMUNICATION WITH S.YAMADA
C
C
      DIMENSION EPOI(16),ANG(11),CRS(11,16)
      DATA ANG/1., 0.866, 0.707, 0.5, 0.259, 0., -0.259, -0.5,
     &         -0.707, -0.866, -1./
      DATA EPOI/270., 290., 310.,
     &          330., 350., 370., 390., 410., 430., 450., 470.,
     &          515., 565., 590., 640., 665./
      DATA CRS/
     &8.16, 8.16, 12.4, 15.2, 21.6, 22.5, 26.0, 23.4, 21.9, 22.4, 22.4,
     &10.4, 10.4, 14.9, 18.5, 22.0, 25.0, 26.5, 25.1, 27.5, 26.9, 26.9,
     &12.7, 12.7, 17.9, 20.4, 23.0, 25.4, 28.6, 28.5, 24.7, 23.5, 23.5,
     &15.5, 15.5, 16.4, 20.0, 22.5, 24.6, 26.6, 22.7, 21.1, 22.4, 22.4,
     &15.4, 15.4, 16.0, 17.0, 19.5, 17.2, 20.3, 19.2, 19.4, 15.2, 15.2,
     &15.4, 15.4, 16.9, 14.8, 17.1, 16.9, 17.6, 15.1, 15.7, 15.7, 15.7,
     &14.4, 14.4, 14.7, 14.0, 12.7, 11.6, 12.1, 9.74, 11.6, 12.2, 12.2,
     &14.0, 14.0, 16.7, 12.7, 12.4, 11.1, 10.7, 8.57, 10.0, 6.91, 6.91,
     &14.9, 14.9, 14.9, 12.1, 11.5, 10.8, 9.34, 8.56, 8.55, 8.54, 8.54,
     &14.6, 14.6, 12.2, 12.8, 9.52, 9.39, 8.89, 8.39, 7.87, 8.14, 8.14,
     &12.8, 12.8, 12.5, 11.4, 10.3, 8.95, 7.66, 8.58, 7.44, 8.10, 8.10,
     &12.3, 12.3, 10.9, 9.18, 8.47, 7.43, 6.59, 5.64, 6.86, 8.07, 8.07,
     &12.4, 12.4, 11.3, 9.69, 7.56, 6.97, 6.38, 6.76, 6.76, 6.76, 6.76,
     &12.6, 12.6, 10.9, 8.84, 7.86, 6.34, 6.33, 6.37, 7.24, 6.97, 6.97,
     &11.2, 11.2, 11.2, 8.71, 7.84, 6.48, 6.31, 6.01, 7.81, 7.84, 7.84,
     &12.1, 12.1, 12.1, 9.29, 8.58, 6.94, 6.31, 6.01, 7.81, 7.84, 7.84/
C
      EFGAMMN=0.
      IF(GK.LE.EPOI(1))RETURN
      IF(GK.GE.EPOI(16))RETURN
      J=1
   10 IF(GK.LE.EPOI(J))GO TO 100
      J=J+1
      GO TO 10
  100 RAT1=(GK-EPOI(J-1))/(EPOI(J)-EPOI(J-1))
      I=2
   20 IF(COST.GE.ANG(I))GO TO 200
      I=I+1
      GO TO 20
  200 RAT2=(COST-ANG(I-1))/(ANG(I)-ANG(I-1))
C
      B1=(1.-RAT1)*CRS(I-1,J-1)+RAT1*CRS(I-1,J)
      B2=(1.-RAT1)*CRS(I,J-1)+RAT1*CRS(I,J)
      EFGAMMN=(1.-RAT2)*B1+RAT2*B2
      RETURN
      END
