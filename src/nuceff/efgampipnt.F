*     -----------------------------------------------------
      FUNCTION efgampipnt(GK)
*     -----------------------------------------------------
*
*     ( purpose )
*       RETURN TOTAL CROSS SECTION OF
C          GAMMA,p ---> pipn
*
*     ( input )
*          GK   : gamma energy(MEV)
*
*     ( output )
*       efgampipnt : TOTAL CROSS SECTION (MICRO BARN/STR.)
*
C
C -- REFERENCE
C      SAID PWA as of Jan-Feb, 2010
C      Solution: SP09K 2700 MEV P(199)
C      (http://gwdac.phys.gwu.edu/analysis/pr_analysis.html)
C                     - P. de Perio
C
      DIMENSION NRG(91),CRS(91)
      DATA NRG/144.,
     &150.,160.,170.,180.,190.,200.,210.,220.,230.,
     &240.,250.,260.,270.,280.,290.,300.,310.,320.,
     &330.,340.,350.,360.,370.,380.,390.,400.,410.,
     &420.,430.,440.,450.,460.,470.,480.,490.,500.,
     &510.,520.,530.,540.,550.,560.,570.,580.,590.,
     &600.,610.,620.,630.,640.,650.,660.,670.,680.,
     &690.,700.,710.,720.,730.,740.,750.,760.,770.,
     &780.,790.,800.,810.,840.,870.,900.,930.,960.,
     &990.,1020.,1050.,1080.,1110.,1140.,1170.,1200.,1230.,
     &1260.,1290.,1320.,1350.,1380.,1410.,1440.,1470.,2000./

      DATA CRS/0,
     &0,62.6,82,92.6,99.9,106,113,120,128,
     &139,152,166,183,201,217,229,235,233,
     &223,209,192,174,159,145,133,124,116,
     &109,104,99.9,96.5,93.8,91.7,90.1,88.8,87.9,
     &87.1,86.5,86.1,85.7,85.5,85.3,85.4,85.6,86,
     &86.6,87.5,88.7,90.1,91.9,94,96.3,99,102,
     &105,108,109,105,101,97.2,92.1,86.3,80.1,
     &74,68.3,63.3,59.2,51.4,49,49.5,51.9,55.5,
     &57.9,56.1,49.6,40.7,32.6,26.6,22.6,20,18.3,
     &17.1,16.3,15.7,15.1,14.6,14,13.5,12.8,5.95/

       efgampipnt=0.
      IF(GK.LT.NRG(1))RETURN
      IF(GK.GT.NRG(91)) then
         efgampipnt=-1.
         RETURN
      ENDIF

      DO 100 I=1,91
         IF (GK.LT.NRG(I)) GOTO 110
 100  CONTINUE

 110  CONTINUE
      efgampipnt= CRS(I-1)
     $       + (CRS(I)-CRS(I-1))/(NRG(I)-NRG(I-1))
     $        *(GK-NRG(I-1))

      RETURN
      END
