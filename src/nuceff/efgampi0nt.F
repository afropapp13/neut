*     -----------------------------------------------------
      FUNCTION efgampi0nt(GK)
*     -----------------------------------------------------
*
*     ( purpose )
*       RETURN TOTAL CROSS SECTION OF
C          GAMMA,n ---> pi0n
*
*     ( input )
*          GK   : gamma energy(MEV)
*
*     ( output )
*       efgampi0nt : TOTAL CROSS SECTION (MICRO BARN/STR.)
*
C
C -- REFERENCE
C      SAID PWA as of Jan-Feb, 2010
C      Solution: SP09K 2700 MEV P(199)
C      (http://gwdac.phys.gwu.edu/analysis/pr_analysis.html)
C                     - P. de Perio
C
      DIMENSION NRG(91),CRS(91)
      DATA NRG/144.,
     &150.,160.,170.,180.,190.,200.,210.,220.,230.,
     &240.,250.,260.,270.,280.,290.,300.,310.,320.,
     &330.,340.,350.,360.,370.,380.,390.,400.,410.,
     &420.,430.,440.,450.,460.,470.,480.,490.,500.,
     &510.,520.,530.,540.,550.,560.,570.,580.,590.,
     &600.,610.,620.,630.,640.,650.,660.,670.,680.,
     &690.,700.,710.,720.,730.,740.,750.,760.,770.,
     &780.,790.,800.,810.,840.,870.,900.,930.,960.,
     &990.,1020.,1050.,1080.,1110.,1140.,1170.,1200.,1230.,
     &1260.,1290.,1320.,1350.,1380.,1410.,1440.,1470.,2000./

      DATA CRS/0,
     &0.363,1.91,4.36,7.78,12.4,18.5,26.7,37.4,51.5,
     &69.9,93.5,123,158,198,238,273,297,306,
     &302,287,267,244,221,199,180,162,147,
     &134,122,111,102,94.1,86.9,80.5,74.8,69.7,
     &65.1,61,57.3,53.9,50.9,48.1,45.6,43.3,41.3,
     &39.5,37.9,36.4,35.2,34.2,33.4,32.8,32.4,32.1,
     &32.1,32.5,33.9,34.4,34.8,34.9,34.8,34.5,33.8,
     &33,31.9,30.7,29.3,25,21,18,16.2,15.5,
     &15.4,15.2,14.1,12.3,10.6,9.39,8.73,8.48,8.53,
     &8.77,9.13,9.54,9.94,10.2,10.4,10.3,10,4.47/

       efgampi0nt=0.
      IF(GK.LT.NRG(1))RETURN
      IF(GK.GT.NRG(91)) then
         efgampi0nt=-1.
         RETURN
      ENDIF

      DO 100 I=1,91
         IF (GK.LT.NRG(I)) GOTO 110
 100  CONTINUE

 110  CONTINUE
      efgampi0nt= CRS(I-1)
     $       + (CRS(I)-CRS(I-1))/(NRG(I)-NRG(I-1))
     $        *(GK-NRG(I-1))

      RETURN
      END
