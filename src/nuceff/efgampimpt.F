*     -----------------------------------------------------
      FUNCTION efgampimpt(GK)
*     -----------------------------------------------------
*     
*     ( purpose )
*       RETURN TOTAL CROSS SECTION OF
C          GAMMA,n ---> pimp
*
*     ( input ) 
*          GK   : gamma energy(MEV)
*
*     ( output ) 
*       efgampimpt : TOTAL CROSS SECTION (MICRO BARN/STR.)
*
C
C -- REFERENCE
C      SAID PWA as of Jan-Feb, 2010
C      Solution: SP09K 2700 MEV P(199)
C      (http://gwdac.phys.gwu.edu/analysis/pr_analysis.html)
C                     - P. de Perio
C
      DIMENSION NRG(91),CRS(91)
      DATA NRG/144.,
     &150.,160.,170.,180.,190.,200.,210.,220.,230.,
     &240.,250.,260.,270.,280.,290.,300.,310.,320.,
     &330.,340.,350.,360.,370.,380.,390.,400.,410.,
     &420.,430.,440.,450.,460.,470.,480.,490.,500.,
     &510.,520.,530.,540.,550.,560.,570.,580.,590.,
     &600.,610.,620.,630.,640.,650.,660.,670.,680.,
     &690.,700.,710.,720.,730.,740.,750.,760.,770.,
     &780.,790.,800.,810.,840.,870.,900.,930.,960.,
     &990.,1020.,1050.,1080.,1110.,1140.,1170.,1200.,1230.,
     &1260.,1290.,1320.,1350.,1380.,1410.,1440.,1470.,2000./

      DATA CRS/0,
     &34,83.2,106,122,135,146,157,168,180,
     &193,208,225,243,261,276,285,286,277,
     &261,240,219,199,181,165,153,142,133,
     &126,120,116,112,108,105,103,101,99.4,
     &97.9,96.7,95.7,94.8,94.2,93.9,93.7,93.7,94,
     &94.4,95.1,95.9,96.8,97.8,98.9,99.8,101,101,
     &101,100,98,94.8,90.6,85.5,79.5,73.2,66.8,
     &60.8,55.5,50.9,47.2,40.3,37.3,35.7,34.5,33.3,
     &32.3,31.1,28.9,25.5,22.2,19.7,17.9,16.6,15.7,
     &15,14.4,13.8,13.4,12.9,12.5,12.1,11.7,9.05/

       efgampimpt=0.
      IF(GK.LT.NRG(1))RETURN
      IF(GK.GT.NRG(91)) then
         efgampimpt=-1.
         RETURN
      ENDIF

      DO 100 I=1,91
         IF (GK.LT.NRG(I)) GOTO 110
 100  CONTINUE

 110  CONTINUE
      efgampimpt= CRS(I-1)
     $       + (CRS(I)-CRS(I-1))/(NRG(I)-NRG(I-1))
     $        *(GK-NRG(I-1))

      RETURN
      END
