*     -----------------------------------------------------
      FUNCTION EFGAMMP(GK,COST)      
*     -----------------------------------------------------
*     
*     ( purpose )
*       RETURN DIFFERENCIAL CROSS SECTION OF
C          GAMMA,P ---> PI+,N
C                     M.NAKAHATA 19-OCT-85
C		   For PI+ data Y.Hayato 16-Mar-94
*
*     ( input ) 
*          GK   : gamma energy(MEV)
*         COST  : COS(THETA)
*     ( output ) 
*       EFGAMMP : DIFFERANCIAL CROSS SECTION (MICRO BARN/STR.)
*
C
C -- REFERENCES
C      INS-REPORT-256   MARCH,1976
C      (Nucl.Phys.B 120(1977) 395)
C
C      PRIVATE COMMUNICATION WITH S.YAMADA
C
      DIMENSION EPOI(17),ANG(11),CRS(11,17)
      DATA ANG/1., 0.866, 0.707, 0.5, 0.259, 0., -0.259, -0.5,
     &         -0.707, -0.866, -1./
      DATA EPOI/250., 270., 290., 310.,
     &          330., 350., 370., 390., 410., 430., 450., 470.,
     &          515., 565., 590., 640., 690./
      DATA CRS/
     &5.91, 5.91, 9.44, 9.32, 13.1, 15.8, 15.8, 15.0, 13.4, 13.8, 13.8,
     &6.81, 6.81, 10.8, 14.5, 18.1, 19.9, 21.3, 19.1, 17.6, 15.5, 15.5,
     &9.95, 9.95, 12.7, 17.8, 19.8, 22.7, 22.5, 20.5, 20.4, 17.9, 17.9,
     &12.0, 12.0, 15.7, 19.6, 22.5, 23.6, 25.4, 22.6, 19.6, 16.6, 16.6,
     &14.2, 14.2, 17.5, 19.5, 21.9, 22.6, 23.0, 20.0, 17.3, 15.7, 15.7,
     &15.0, 15.0, 15.8, 17.3, 18.2, 17.2, 18.6, 16.1, 14.1, 11.1, 11.1,
     &15.0, 15.0, 15.5, 15.5, 16.8, 15.6, 14.7, 13.0, 11.3, 11.3, 11.3,
     &14.2, 14.2, 13.8, 13.9, 13.0, 11.2, 9.68, 8.07, 7.68, 7.72, 7.72,
     &12.4, 12.4, 14.1, 12.5, 11.2, 11.0, 9.00, 6.88, 6.39, 4.42, 4.42,
     &12.9, 12.9, 12.9, 12.1, 10.8, 9.50, 7.44, 6.32, 5.47, 4.62, 4.62,
     &14.0, 14.0, 12.6, 12.9, 9.08, 7.80, 6.80, 5.80, 4.81, 4.53, 4.53,
     &12.3, 12.3, 12.1, 11.2, 9.84, 7.48, 6.39, 5.74, 3.98, 3.77, 3.77,
     &11.8, 11.8, 11.3, 10.2, 8.81, 7.22, 5.63, 3.87, 3.62, 3.36, 3.36,
     &11.2, 11.2, 11.9, 10.6, 9.03, 7.15, 5.54, 4.48, 4.48, 4.48, 4.48,
     &12.0, 12.0, 11.8, 10.4, 9.03, 7.06, 5.71, 4.04, 3.59, 3.43, 3.43,
     &12.4, 12.4, 12.4, 11.1, 9.61, 7.21, 6.14, 5.11, 3.71, 3.17, 3.17,
     &11.3, 11.3, 12.3, 13.1, 12.0, 9.47, 7.57, 5.90, 4.56, 3.82, 3.82/
C
      EFGAMMP=0.
      IF(GK.LE.EPOI(1))RETURN
      IF(GK.GE.EPOI(17))RETURN
      J=1
   10 IF(GK.LE.EPOI(J))GO TO 100
      J=J+1
      GO TO 10
  100 RAT1=(GK-EPOI(J-1))/(EPOI(J)-EPOI(J-1))
      I=2
   20 IF(COST.GE.ANG(I))GO TO 200
      I=I+1
      GO TO 20
  200 RAT2=(COST-ANG(I-1))/(ANG(I)-ANG(I-1))
C
      B1=(1.-RAT1)*CRS(I-1,J-1)+RAT1*CRS(I-1,J)
      B2=(1.-RAT1)*CRS(I,J-1)+RAT1*CRS(I,J)
      EFGAMMP=(1.-RAT2)*B1+RAT2*B2
      RETURN
      END
