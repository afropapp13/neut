      FUNCTION PASSL(DENS,SIGM)
C
C   MEAN FREE PASS OF MESON IN THE NUCLEUS
C          DENS : NUCLEON DENSITY  /FM**3
C          SIGM : SROSS SECTION (MB)
C  T.KAJITA  19-OCT-85
C
      IF(SIG.LE.0.)SIG=0.00001
      IF(DENS.LE.0.)DENS=0.000001
      SIG=SIGM/10.
C          THE 10 MEANS MB=1/10 *(FM)**2
      PASSL=1./(SIG*DENS)
C
      RETURN
      END
